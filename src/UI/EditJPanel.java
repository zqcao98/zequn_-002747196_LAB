    public void displayProductList() {
        
        for (Product p: this.delPackage.getProductList()) {
            jComboBox1.addItem(p.getProductId());
        }
    }
    
        public void displayPackageDetails() {
        jTextField1.setText(String.valueOf(this.delPackage.getPackageId()));
        jTextField2.setText(String.valueOf(this.delPackage.getPackageWeight()));

        Customer customer = this.delPackage.getCustomer();
        jTextField7.setText(String.valueOf(customer.getCustomerId()));
        jTextField6.setText(customer.getFullName());

    }
        
    private void showBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showBtnActionPerformed

        jTextField3.setEnabled(false);
        
        int pid = (int) jComboBox1.getSelectedItem();


        Product prod = this.delPackage.findProduct(pid);
         this.productViewed = prod;


        if (!prod.equals(null)) {
            jTextField3.setText(String.valueOf(pid));
            jTextField4.setText(prod.getProductName());
            jTextField5.setText(String.valueOf(prod.getPrice()));
        } else {
            JOptionPane.showMessageDialog(null, "Product not found");
        }
        
    }//GEN-LAST:event_showBtnActionPerformed

    
