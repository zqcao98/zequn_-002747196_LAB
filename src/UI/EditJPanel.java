    private void UpdateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateBtnActionPerformed
        // TODO add your handling code here:

        //Get the data;
        String packageId = jTextField1.getText();
        String weight = jTextField2.getText();

        String customerId = jTextField7.getText();
        String customerName = jTextField6.getText();

        //Store the data;
        this.delPackage.setPackageId(Integer.valueOf(packageId));
        this.delPackage.setPackageWeight(Double.valueOf(weight));

        Customer customer = this.delPackage.getCustomer();
        customer.setFullName(customerName);
        customer.setCustomerId(Integer.valueOf(customerId));
        
    }//GEN-LAST:event_UpdateBtnActionPerformed

